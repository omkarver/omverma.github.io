        <p class="font1">I have written before about two books—An American Marriage and The New Jim Crow—that really
            broadened my understanding of some of the hardships faced by Black people in America. There’s a movie out
            now called Time that ranks up there with both of them. It was nominated for Best Documentary at the Academy
            Awards this year, and I can see why. I can’t recommend it highly enough.</p>
        <p class="font1">I saw Time at the Sundance Film Festival last year (before the COVID-19 pandemic shut things
            down) on the recommendation of a friend who served as an executive producer on the movie. It is available
            for streaming now on Amazon Prime; in fact, you can watch it for free this week, even if you don’t have a
            Prime subscription.</p>
        <p class="font1">On the surface, Time is about a Black mother of six named Sibil Fox Richardson who is trying to
            get her husband, Robert, out of state prison in Louisiana. He was sentenced to 60 years for armed robbery;
            much of the movie takes place at a time when he has been in prison nearly 18 years. Sibil—who also goes by
            the name Fox Rich—participated in the robbery and served three and a half years herself.</p>
        <p class="font1">The movie is not about whether Robert and Sibil committed the crime. They did. It is partly
            about the impact on a family of locking up a father for 60 years—and very much about their family’s
            resilience.</p>

        <p class="font1">Many documentaries I’ve seen lay out all the facts in a clear storyline: this happened, then
            that happened. Time doesn’t work that way. The director, Garrett Bradley, is not interested in explaining
            every fact. You learn only a few details about the robbery and their separate trials, although there is a
            long sequence in which Sibil talks about making amends with the victims of their crime.</p>

        <p class="font1">Although Time doesn’t tell you much, it shows you a lot. It is a poetic portrait of a family
            who love and support each other despite their difficult circumstances. In one scene, one of the couple’s
            sons graduates from a dental program. But the movie doesn’t take time to explain what kind of degree he got,
            or much of anything else. It is all about the joy of his accomplishment and how the family comes together to
            celebrate. The details are not as important as the way they show up for each other.</p>


        <p class="font1">
            The same is true of the movie’s portrayal of Sibil’s efforts to help Robert and speak out more broadly
            against mass incarceration. She is repeatedly told that there is no news about her husband’s case. There are
            painfully long scenes of her waiting on the phone to speak to a bureaucrat about a judge’s ruling. It is not
            always quite clear what the particular ruling in question might be, but again, that is not what matters. The
            point is how impersonally and inhumanely the system treats everyone caught up in it, including the offenders
            and their families, and how much time and energy are required to navigate it. This idea is important in
            American Marriage and New Jim Crow too, but with this movie, you see it first-hand </p>



